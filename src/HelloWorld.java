/*多行注释用这个*/
//单行注释用这个
//第一行的第三个单词必须和所在的文件名称完全一样，大小写也一样
public class HelloWorld {
    //HelloWorld表示定义的一个类的名称，类是java当中所有源代码的基本组织单位
    public static void main(String[] args){
        //第二行的内容是万年不变的固定写法，代表main方法
        //这一行代表程序执行的起点
        System.out.println("Hello,World!");//打印输出语句
        // System.out.println也是万年不变的

    }
}
